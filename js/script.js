let currentLang = localStorage.getItem('lang') || 'ro';

// ---- THEME ----
function applyTheme(theme) {
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem('theme', theme);
  const btn = document.getElementById('themeToggle');
  if (btn) btn.textContent = theme === 'dark' ? '☀ Light' : '◑ Dark';
}

function toggleTheme() {
  const cur = document.documentElement.getAttribute('data-theme') || 'light';
  applyTheme(cur === 'dark' ? 'light' : 'dark');
}

// ---- LANGUAGE ----
function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  const t = translations[lang] || translations['ro'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (t[k] !== undefined) el.textContent = t[k];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const k = el.getAttribute('data-i18n-placeholder');
    if (t[k] !== undefined) el.placeholder = t[k];
  });
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
}

// ---- VALIDATION HELPERS ----
function validateEmail(email) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
}

function showMsg(container, text, type) {
  const div = document.createElement('div');
  div.className = `msg msg-${type}`;
  div.textContent = text;
  const existing = container.querySelector('.msg');
  if (existing) existing.remove();
  container.insertBefore(div, container.firstChild);
  if (type === 'success') setTimeout(() => div.remove && div.remove(), 5000);
}

// ---- FORM VALIDATION ----
function validateLoginForm(e) {
  const t = translations[currentLang] || translations['ro'];
  const form = e.target;
  const email = form.querySelector('[name="email"]').value.trim();
  const pass  = form.querySelector('[name="password"]').value;
  const container = form.closest('.form-container') || form.parentElement;
  if (!email || !pass) { e.preventDefault(); showMsg(container, t.err_empty, 'error'); return false; }
  if (!validateEmail(email)) { e.preventDefault(); showMsg(container, t.err_email, 'error'); return false; }
  return true;
}

function validateRegisterForm(e) {
  const t = translations[currentLang] || translations['ro'];
  const form = e.target;
  const name  = form.querySelector('[name="username"]').value.trim();
  const email = form.querySelector('[name="email"]').value.trim();
  const pass  = form.querySelector('[name="password"]').value;
  const pass2 = form.querySelector('[name="password2"]') ? form.querySelector('[name="password2"]').value : pass;
  const container = form.closest('.form-container') || form.parentElement;
  if (!name || !email || !pass) { e.preventDefault(); showMsg(container, t.err_empty, 'error'); return false; }
  if (!validateEmail(email)) { e.preventDefault(); showMsg(container, t.err_email, 'error'); return false; }
  if (pass.length < 6) { e.preventDefault(); showMsg(container, t.err_pass_short, 'error'); return false; }
  if (pass !== pass2) { e.preventDefault(); showMsg(container, t.err_pass_match, 'error'); return false; }
  return true;
}

function validateContactForm(e) {
  const t = translations[currentLang] || translations['ro'];
  const form = e.target;
  const name  = form.querySelector('[name="name"]').value.trim();
  const email = form.querySelector('[name="email"]').value.trim();
  const msg   = form.querySelector('[name="message"]').value.trim();
  const container = form.closest('.form-container') || form.parentElement;
  if (!name || !email || !msg) { e.preventDefault(); showMsg(container, t.err_empty, 'error'); return false; }
  if (!validateEmail(email)) { e.preventDefault(); showMsg(container, t.err_email, 'error'); return false; }
  return true;
}

function validateReservationForm(e) {
  const t = translations[currentLang] || translations['ro'];
  const form = e.target;
  const checkin  = form.querySelector('[name="checkin"]');
  const checkout = form.querySelector('[name="checkout"]');
  const guests   = form.querySelector('[name="guests"]');
  const container = form.closest('.modal') || form.parentElement;
  if (!checkin.value || !checkout.value || !guests.value) {
    e.preventDefault(); showMsg(container, t.err_empty, 'error'); return false;
  }
  if (new Date(checkout.value) <= new Date(checkin.value)) {
    e.preventDefault(); showMsg(container, t.err_date, 'error'); return false;
  }
  return true;
}

// ---- MOBILE NAV ----
function initHamburger() {
  const btn = document.getElementById('hamburger');
  const links = document.getElementById('navLinks');
  if (btn && links) btn.addEventListener('click', () => links.classList.toggle('open'));
}

// ---- MODAL HELPERS ----
function closeIfOverlay(e, id) {
  if (e.target === e.currentTarget) document.getElementById(id).classList.remove('open');
}

// ---- SET MIN DATE ON DATE INPUTS ----
function initDateInputs() {
  const today = new Date().toISOString().split('T')[0];
  document.querySelectorAll('input[name="checkin"]').forEach(el => el.min = today);
  document.querySelectorAll('input[name="checkout"]').forEach(el => el.min = today);

  document.querySelectorAll('input[name="checkin"]').forEach(el => {
    el.addEventListener('change', () => {
      const out = el.closest('form').querySelector('[name="checkout"]');
      if (out) out.min = el.value;
    });
  });
}

// ---- INIT ----
document.addEventListener('DOMContentLoaded', () => {
  applyTheme(localStorage.getItem('theme') || 'light');
  applyLanguage(currentLang);

  const themeBtn = document.getElementById('themeToggle');
  if (themeBtn) themeBtn.addEventListener('click', toggleTheme);

  const langSel = document.getElementById('langSelect');
  if (langSel) langSel.addEventListener('change', e => applyLanguage(e.target.value));

  const loginForm = document.getElementById('loginForm');
  if (loginForm) loginForm.addEventListener('submit', validateLoginForm);

  const registerForm = document.getElementById('registerForm');
  if (registerForm) registerForm.addEventListener('submit', validateRegisterForm);

  const contactForm = document.getElementById('contactForm');
  if (contactForm) contactForm.addEventListener('submit', validateContactForm);

  document.querySelectorAll('.reservationForm').forEach(f => {
    f.addEventListener('submit', validateReservationForm);
  });

  initHamburger();
  initDateInputs();
});